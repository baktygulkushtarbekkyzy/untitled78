public class Circle {
    private int Pi;


    static void area(int radius){
        System.out.print("Tegerektin ayanty: ");
        System.out.println(Math.PI*(radius*radius));
    }
    static void circumference(int radius){
        System.out.print("Tegerektin uzundugu: ");
        System.out.println(Math.PI*2*radius);

    }
}
